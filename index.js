function multiply(num1, num2) {
    const product = num1 * num2
    return product
}

function subtraction(num1, num2) {
    let difference = num1 - num2
    return difference
}

function addition(num1, num2) {
    let sum = num1 + num2
    return sum
}

function division(num1, num2) {
    let div = num1 / num2
    return div
}